<script>
    const charactersList = document.getElementById('charactersList');
    const searchBar = document.getElementById('searchBar');
    let hpCharacters = [];

    searchBar.addEventListener('keyup', (e) => {
        const searchString = e.target.value.toLowerCase();

        const filteredCharacters = hpCharacters.filter((character) => {
            return (
                character.name.toLowerCase().includes(searchString) ||
                character.designation.toLowerCase().includes(searchString)
            );
        });
        displayCharacters(filteredCharacters);
    });

    const loadCharacters = async () => {
        try {
            const res = await fetch('etdata.json');
            hpCharacters = await res.json();
            displayCharacters(hpCharacters);
        } catch (err) {
            console.error(err);
        }
    };

    const displayCharacters = (etdata) => {
        const htmlString = etdata
            .map((character) => {
                return `
                <li class="character">
                    <h2><a href="${character.page}">${character.name}</a></h2>
                    <p>${character.designation}</p>
                    <p>Email : ${character.email}</p>
                    <p>Contact No. : ${character.contactNo} Intercom - ${character.intercomNo}</p>
                    <img src="${character.img}"></img>
                </li>
            `;
            })
            .join('');
        charactersList.innerHTML = htmlString;
    };

    loadCharacters();

</script>






